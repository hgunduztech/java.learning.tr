public class gun1 {
    public static void main(String[] args) {
        // Tam sayı veri türü (integer)
        int age = 25; // Kişinin yaşını tutan tam sayı değişkeni

        // Ondalıklı sayı veri türü (double)
        double height = 1.75; // Kişinin boyunu tutan ondalıklı sayı değişkeni

        // Tek karakter veri türü (char)
        char initial = 'A'; // Kişinin adının baş harfini tutan karakter değişkeni

        // Metin veri türü (String)
        String name = "Azat"; // Kişin"in adını tutan metin değişkeni

        // Doğru veya yanlış veri türü (boolean)
        boolean isStudent = true; // Kişinin öğrenci olup olmadığını tutan doğru/yanlış değişkeni

        // Değişkenleri ekrana yazdır
        System.out.println("Yaş: " + age); // "Yaş: 25" şeklinde ekrana yazdırır
        System.out.println("Boy: " + height); // "Boy: 1.75" şeklinde ekrana yazdırır
        System.out.println("İlk Harf: " + initial); // "İlk Harf: A" şeklinde ekrana yazdırır
        System.out.println("İsim: " + name); // "İsim: Azat" şeklinde ekrana yazdırır
        System.out.println("Öğrenci mi?: " + isStudent); // "Öğrenci mi?: true" şeklinde ekrana yazdırır
    }
}
